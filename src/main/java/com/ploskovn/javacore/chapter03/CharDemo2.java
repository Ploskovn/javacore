// Символьные переменные ведут себя как целочисленные значения
class CharDemo2 {
	
	public static void main(String args[] ) {
		char chl;
		
		chl = 'X';
		System.out.println("ch1 содержит " + chl) ;
		
		chl++; // увеличить на единицу значение переменной ch1
		System.out.println("chl теперь содержит " + chl);
	}
}
